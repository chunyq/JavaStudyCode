public class Hello{
	public static void main(String[]args){
		// \"  \' 转义字符
		System.out.println("hello-中\"文￥%#@……"); // 字符串  0-n
		System.out.println(""); 
		System.out.println('a'); //字符：'' 只能是一个字符
		System.out.println('\'');
		System.out.println(12); //整数
		System.out.println(-12.9); //小数 浮点数
		System.out.println(true); //布尔类型
		System.out.println(false); 
			
		System.out.println("姓名：张三"+"\t年龄：18岁"); // 字符串  0-n
		
		System.out.println("姓名：张三"+"\t年龄："+18 );
				
	}
}