/*
算术运算符的使用
 */
public class ArithmeticOperator{
	public static void main(String[] args) {
		//  /和%的操作
		int a = 10;
		int b = 4;
		System.out.println(a/b); // 10/4 整除得2 
		System.out.println(a%b);
		System.out.println(10.0/b); //10.0/4 = 2.5

		// +号的使用
		// char类型参与算术运算
		// ASCII码'a' -- 97  'A' -- 65  '0' -- 48
		
	}
}